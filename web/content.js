function slashCommandTable(lang) {
  const label = lang === 'ru' ? 'Зачем нужна' : 'What it is for'
  const commandLabel = lang === 'ru' ? 'Команда' : 'Command'
  const rows = SLASH_COMMANDS
    .map(c => `| \`${c.cmd}\` | ${lang === 'ru' ? c.ru : c.en} |`)
    .join('\n')
  return `| ${commandLabel} | ${label} |\n|---------|----------|\n${rows}`
}

