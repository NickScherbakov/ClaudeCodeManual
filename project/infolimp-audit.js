// project/infolimp-audit.js
// Цель: существует список причин отсутствия трафика на infolimp.ru
//       с конкретными действиями и приоритетами
//
// Узел 1 ЦЕЛЬ:     диагностика + план действий
// Узел 2 ЗАДАЧИ:   A читать сайт → B три угла параллельно → C конкуренты → D план
// Узел 3 ПРОЦЕССЫ: A → (B parallel) → C → D последовательно
// Узел 4 РЕСУРСЫ:  WebFetch/WebSearch агенты + schema отчёт

export const meta = {
  name: 'infolimp-audit',
  description: 'Аудит infolimp.ru: диагностика отсутствия трафика + план действий',
  phases: [
    { title: 'Read',    detail: 'Читаем infolimp.ru — главная и ключевые страницы' },
    { title: 'Analyze', detail: 'Технический аудит | Контент | SEO структура' },
    { title: 'Compare', detail: 'Конкуренты в нише' },
    { title: 'Plan',    detail: 'Приоритетный план действий' },
  ],
}

const ISSUE_SCHEMA = {
  type: 'object',
  properties: {
    issues: {
      type: 'array',
      items: {
        type: 'object',
        properties: {
          category: { enum: ['technical', 'content', 'seo', 'competitor'] },
          title:    { type: 'string', description: 'Краткое название проблемы' },
          detail:   { type: 'string', description: 'Объяснение почему это мешает трафику' },
          action:   { type: 'string', description: 'Конкретное действие для исправления' },
          priority: { enum: ['critical', 'high', 'medium', 'low'] },
        },
        required: ['category', 'title', 'action', 'priority'],
      },
    },
  },
  required: ['issues'],
}

// ── A: Читаем сайт ────────────────────────────────────────────────────────────
phase('Read')
log('Читаю infolimp.ru...')

const siteContent = await agent(
  `Используй WebFetch чтобы прочитать https://infolimp.ru
   Затем найди и прочитай 3-5 внутренних страниц (статьи, разделы, категории).
   Верни:
   - Заголовок сайта и описание (из мета-тегов если есть)
   - Тематика: о чём сайт, для кого
   - Список страниц которые нашёл с их URL
   - Краткое содержание каждой страницы
   - Title и description каждой страницы (если видишь в HTML)
   - Объём контента: примерно сколько текста на страницах`,
  { label: 'site-reader' }
)

log('Сайт прочитан. Запускаю параллельный анализ...')

// ── B: Три угла параллельно ───────────────────────────────────────────────────
phase('Analyze')

const analyses = await parallel([
  () => agent(
    `Ты технический SEO аудитор. Проанализируй сайт infolimp.ru на основе этих данных:

     ${siteContent}

     Проверь и найди проблемы в:
     - Мета-теги (title/description): есть ли, правильная ли длина (title 50-60 симв, description 150-160)
     - Структура URL: чистые ли, есть ли кириллица, дублирование
     - Внутренняя перелинковка: связаны ли страницы между собой
     - Скорость и объём: тяжёлые ли страницы, много ли изображений без сжатия
     - Технические сигналы: упомяни если видишь признаки отсутствия sitemap/robots

     Для каждой проблемы — конкретное действие как исправить.`,
    { label: 'technical', phase: 'Analyze', schema: ISSUE_SCHEMA }
  ),
  () => agent(
    `Ты контент-стратег. Проанализируй сайт infolimp.ru на основе этих данных:

     ${siteContent}

     Оцени и найди проблемы в:
     - Ценность для читателя: есть ли реальная польза или общие слова
     - Глубина: поверхностный контент (< 500 слов) или детальный (> 1500 слов)
     - Уникальность: похоже ли на рерайт других сайтов
     - Ниша: есть ли чёткая тема или сайт обо всём
     - Регулярность: признаки активного или заброшенного сайта
     - Целевая аудитория: понятно ли для кого сайт

     Для каждой проблемы — конкретное действие как исправить.`,
    { label: 'content', phase: 'Analyze', schema: ISSUE_SCHEMA }
  ),
  () => agent(
    `Ты SEO специалист. Проанализируй сайт infolimp.ru на основе этих данных:

     ${siteContent}

     Найди проблемы в:
     - Заголовки H1/H2/H3: структура, ключевые слова, дублирование
     - Ключевые слова: используются ли в тексте, title, description
     - Семантическое ядро: написаны ли страницы под реальные поисковые запросы
     - Alt тексты изображений: есть ли, с ключевыми словами ли
     - Анкоры внутренних ссылок: информативные или "нажмите здесь"
     - Длина и структура текстов под поисковую оптимизацию

     Для каждой проблемы — конкретное действие как исправить.`,
    { label: 'seo', phase: 'Analyze', schema: ISSUE_SCHEMA }
  ),
])

const analysiIssues = analyses.filter(Boolean).flatMap(r => r.issues || [])
log(`Анализ завершён. Найдено проблем: ${analysiIssues.length}. Ищу конкурентов...`)

// ── C: Конкуренты ─────────────────────────────────────────────────────────────
phase('Compare')

const competition = await agent(
  `Используй WebSearch чтобы исследовать конкурентов сайта infolimp.ru.

   Тематика сайта (из анализа): ${siteContent.slice(0, 600)}

   Шаг 1: Определи главную тему сайта и 2-3 ключевых запроса по которым он должен ранжироваться.
   Шаг 2: Поищи в Google топ-5 сайтов по этим запросам.
   Шаг 3: Для каждого конкурента объясни почему он выше (объём, авторитет, ниша, качество контента).
   Шаг 4: Найди что есть у конкурентов чего нет у infolimp.ru — это и есть разрыв для роста.

   Каждый разрыв — отдельная проблема с конкретным действием.`,
  { label: 'competitors', schema: ISSUE_SCHEMA }
)

const allIssues = [
  ...analysiIssues,
  ...(competition?.issues || []),
]

log(`Всего проблем: ${allIssues.length}. Составляю план...`)

// ── D: План действий ──────────────────────────────────────────────────────────
phase('Plan')

const plan = await agent(
  `Ты консультант по развитию сайта. Сайт infolimp.ru не получает трафик.

   Вот все найденные проблемы:
   ${JSON.stringify(allIssues, null, 2)}

   Составь практичный план:
   1. БЫСТРЫЕ ПОБЕДЫ (до 1 недели): что даст результат быстрее всего
   2. ОСНОВНАЯ РАБОТА (1-4 недели): ключевые изменения для роста
   3. ДОЛГОСРОЧНО (1-3 месяца): что строить системно

   Для каждого пункта: конкретное действие (не "улучшить контент" а "написать статью на тему X длиной 1500+ слов"),
   ожидаемый эффект, как проверить что сделано правильно.

   Начни с самого важного.`,
  { label: 'action-plan' }
)

const counts = { critical: 0, high: 0, medium: 0, low: 0 }
for (const i of allIssues) {
  if (counts[i.priority] !== undefined) counts[i.priority]++
}

log(`Готово. Проблем: critical:${counts.critical} high:${counts.high} medium:${counts.medium} low:${counts.low}`)

return {
  overview: siteContent,
  issues: allIssues,
  counts,
  action_plan: plan,
}
